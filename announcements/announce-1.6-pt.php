<h2>
  O KDE Lan&ccedil;a uma Nova Vers&atilde;o do Pacote Integrado de Escrit&oacute;rio Livre
</h2>
<p align="justify">16 de Outubro de 2006 (INTERNET).
  O <a href="http://www.kde.org/">Projecto KDE</a> anunciou hoje o lan&ccedil;amento da
  vers&atilde;o 1.6 do <a href="http://www.koffice.org/">KOffice</a>, uma
  vers&atilde;o <a href="http://developer.kde.org/development-versions/koffice-1.6-release-plan.html">h&aacute; muito esperada</a>
  e que traz grandes melhorias num conjunto de &aacute;reas.
</p>

<h2>Pontos Fortes</h2>
<p align="justify">
  O foco de energia da equipa do KOffice est&aacute; actualmente na vers&atilde;o
  revolucion&aacute;ria - a 2.0 -, que ser&aacute; baseada no KDE 4 e no Qt 4.
  A vers&atilde;o 1.6 pretende ser principalmente uma vers&atilde;o de funcionalidades
  espec&iacute;ficas para os componentes com desenvolvimento mais r&aacute;pido:
  o Krita e o Kexi.  Contudo, est&atilde;o outros componentes tamb&eacute;m a ser desenvolvidos
  de forma activa, tendo alguns resultados espantosos em alguns casos.
  Aqui est&atilde;o as caracter&iacute;sticas principais desta vers&atilde;o:
</p>

<h3>O Krita Avan&ccedil;a para o Dom&iacute;nio da Edi&ccedil;&atilde;o de Imagem Profissional</h3>
<p align="justify">
  Com esta vers&atilde;o, o <a href="http://www.koffice.org/krita/">Krita</a> avan&ccedil;a e torna-se
  utiliz&aacute;vel na edi&ccedil;&atilde;o profissional de imagens. O Krita e o seu respons&aacute;vel de manuten&ccedil;&atilde;o
  ganharam o pr&eacute;mio de "Melhor Aplica&ccedil;&atilde;o" na confer&ecirc;ncia do KDE deste ano, em Dublin.
  Gra&ccedil;as a funcionalidades como a selec&ccedil;&atilde;o magn&eacute;tica, as camadas de efeitos, a independ&ecirc;ncia
  de modelos de cores e a capacidade completa de programa&ccedil;&atilde;o, evoluiu para o que poder&aacute; vir
  a ser o melhor programa livre de edi&ccedil;&atilde;o de imagem dos dias de hoje.

  Para mais detalhes, veja abaixo nas Melhorias Principais ou o

  <a href="http://www.koffice.org/announcements/changelog-1.6-final.php">registo de altera&ccedil;&otilde;es completo</a>.
</p>
<h3>O Kexi Acrescenta Muitas Funcionalidades Novas</h3>
<p align="justify">
    O Kexi, uma aplica&ccedil;&atilde;o de bases de dados para o ambiente de trabalho que
    tenta competir com o MS Access, &eacute; a outra aplica&ccedil;&atilde;o no KOffice que j&aacute; se
    tornou a melhor no seu segmento. O Kexi recebeu cerca de 270 melhorias
    desde a vers&atilde;o 1.0 (KOffice 1.5). Com esta vers&atilde;o, o Kexi ganha algumas
    funcionalidades como a capacidade de <a href="http://www.kdedevelopers.org/node/2163">lidar com imagens</a>, 
    <a href="http://www.kexi.pl/media/en/compact_db/">compactar as bases de dados</a>, 
    o reconhecimento autom&aacute;tico do tipo de dados e as ferramentas de programa&ccedil;&atilde;o
    do Kross. Outras partes foram tamb&eacute;m bastante desenvolvidas, tendo em mente
    as funcionalidades novas e a facilidade de utiliza&ccedil;&atilde;o.
</p>

<h3>O KFormula Passou para OpenDocument e MathML</h3>
<p align="justify">
  O editor de f&oacute;rmulas do KOffice suporta agora o OpenDocument e o MathML e usa-os
  como formatos de ficheiros por omiss&atilde;o. Ultrapassa tamb&eacute;m o componente equivalente
  do OpenOffice.org, pontuando 70% no
      <a href="http://www.w3.org/Math/testsuite">conjunto de testes de MathML do W3C</a>, 
  face aos 22% do OpenOffice.org Formula.
  A equipa do KOffice v&ecirc; este como o primeiro exemplo onde o trabalho para oferecer uma
  base de c&oacute;digo bem estruturada do KOffice compensa na cria&ccedil;&atilde;o de um suporte superior
  para a norma existente.
</p>

<h3>Suporte de Programa&ccedil;&atilde;o no KSpread, Krita e Kexi</h3>
<p align="justify">
    O KOffice 1.6 traz o suporte de programa&ccedil;&atilde;o a um novo n&iacute;vel, com a implementa&ccedil;&atilde;o
    desta funcionalidade no KSpread, Krita e Kexi. A programa&ccedil;&atilde;o &eacute; oferecida atrav&eacute;s
    da interface de programa&ccedil;&atilde;o multi-linguagem <a href="http://dot.kde.org/1152490640/">Kross</a>,
    que permite ao KOffice ser programado em Python, Ruby e com extens&otilde;es futuras
    poss&iacute;veis em Javascript e Java.  Com esta vers&atilde;o, o KOffice introduz tamb&eacute;m
    a programa&ccedil;&atilde;o sem GUI onde, p.ex., os documentos das folhas de c&aacute;lculo poder&atilde;o
    ser manipulados facilmente com estes programas para criar
    <a href="http://www.robweir.com/blog/2006/09/odf-twenty-patterns-of-use.html">muitos casos de uso novos</a>.
</p>

<h2>
  Melhorias Principais
</h2>
<p align="justify">
  As principais melhorias no KOffice 1.6 sobre o KOffice 1.5 s&atilde;o:
</p>
    <strong>KSpread</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        Adi&ccedil;&atilde;o do suporte de programa&ccedil;&atilde;o com o Kross.
        </div>
      </li>
    </ul>
  <p />
    <strong>Kexi (Aplica&ccedil;&atilde;o de base de dados)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        Diversas melhorias e optimiza&ccedil;&otilde;es na biblioteca de conectividade de base de dados.
        </div>
      </li>
      <li>
        <div align="justify">
	Diversas melhorias no Desenhador de Tabelas: tipo de dados "Imagem",
      comandos para desfazer e refazer,
      suporte para valores predefinidos e dicas de ferramentas.
        </div>
      </li>
      <li>
        <div align="justify">
       Melhorias no Desenhador de Pesquisas:
       defini&ccedil;&atilde;o da direc&ccedil;&atilde;o de ordena&ccedil;&atilde;o das colunas, tanto no modo visual como de SQL
        </div>
      </li>
      <li>
        <div align="justify">
        Elemento gr&aacute;fico &quot;&Aacute;rea de Imagem&quot; para dados, na Janela de Formul&aacute;rios e Tabelas,
        e grandes melhorias no elemento do Campo Autom&aacute;tico nos formul&aacute;rios
        </div>
      </li>
      <li>
        <div align="justify">
        Suporte para macros
        </div>
      </li>
    </ul>
  <p />
    <strong>Krita (Editor de imagens baseadas em pixels)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        Suporte para lidar com as perspectivas nos desenhos, incluindo as transforma&ccedil;&otilde;es, grelhas e clonagens em perspectiva
        </div>
      </li>
      <li>
        <div align="justify">
        Adi&ccedil;&atilde;o do suporte para as m&aacute;scaras de camadas
        </div>
      </li>
      <li>
        <div align="justify">
        Foi criada uma nova ferramenta de selec&ccedil;&atilde;o magn&eacute;tica.
        </div>
      </li>
	
      <li>
        <div align="justify">
        Foram adicionados muitos filtros novos:
        ru&iacute;do aleat&oacute;rio, extrac&ccedil;&atilde;o aleat&oacute;ria, correc&ccedil;&atilde;o de lentes, filtros do krita-plugins
        </div>
      </li>
      <li>
        <div align="justify">
        O Krita consegue abrir ficheiros PDF e transform&aacute;-los numa imagem
        </div>
      </li>
      <li>
        <div align="justify">
        Foi adicionada uma ferramenta para lidar com curvas B&eacute;zier
        </div>
      </li>
    </ul>
  <p />
    <strong>KPlato (Ferramenta de gest&atilde;o de projectos)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        Foi adicionado o c&aacute;lculo PERT para estimativas de valores baseados em valores optimistas, esperados e pessimistas
        </div>
      </li>
      <li>
        <div align="justify">
        A distribui&ccedil;&atilde;o usada depende da configura&ccedil;&atilde;o do Risco.
        </div>
      </li>
    </ul>
  <p />
    <strong>KFormula (Editor de f&oacute;rmulas matem&aacute;ticas)</strong>
    <ul type="disc">
      <li>
        <div align="justify">
        O OpenDocument e o MathML s&atilde;o agora o formato nativo, sendo o OpenDocument o predefinido
        </div>
      </li>
      <li>
        <div align="justify">
        Lista de elementos suportados: mi, mn, mtext, ms, mstyle, msqrt, mroot, mfrac, mrow (outros poder&atilde;o funcionar)
        </div>
      </li>
      <li>
        <div align="justify">
        Lista de estilos suportados: mathcolor, mathbackground, mathsize, mathvariant
        </div>
      </li>
    </ul>
  <p />
    <strong>Kugar (Ferramenta de relat&oacute;rios)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        Os modelos do Kugar ficam agora vis&iacute;veis na janela inicial.
        </div>
      </li>
    </ul>
  <p />
<p align="justify">
  Est&aacute; dispon&iacute;vel uma 
  <a href="http://www.koffice.org/announcements/changelog-1.6.php">lista de altera&ccedil;&otilde;es</a>
   na <a href="http://www.koffice.org/">p&aacute;gina Web</a> do KOffice.
</p>

<h2>Acerca do KOffice</h2>
  <p align="justify">
    O KOffice &eacute; um pacote de escrit&oacute;rio integrado, estando mais vocacionado nos dias 
    de hoje para o KDE, que utiliza normas livres e abertas para os seus formatos
    de documentos, a comunica&ccedil;&atilde;o de componentes e a incorpora&ccedil;&atilde;o de componentes.
  </p>
  <p align="justify">
    Esta vers&atilde;o inclui:
</p>
<ul>
  <li>um processador de texto completo e baseado em molduras
    (<a href="http://www.koffice.org/kword/">KWord</a>);
  </li>
  <li>uma folha de c&aacute;lculo
    (<a href="http://www.koffice.org/kspread/">KSpread</a>);
  </li>
  <li>uma aplica&ccedil;&atilde;o para apresenta&ccedil;&otilde;es
    (<a href="http://www.koffice.org/kpresenter/">KPresenter</a>);
  </li>
  <li>uma aplica&ccedil;&atilde;o de fluxogramas
    (<a href="http://www.thekompany.com/projects/kivio/">Kivio</a>);
  </li>
  <li>uma aplica&ccedil;&atilde;o de bases de dados integrada
    (<a href="http://www.koffice.org/kexi/">Kexi</a>);
  </li>
  <li>uma aplica&ccedil;&atilde;o nova de gest&atilde;o de projectos
    (<a href="http://www.koffice.org/kplato/">KPlato</a>).
  </li>
  <li>uma aplica&ccedil;&atilde;o de pintura e edi&ccedil;&atilde;o de imagens rasterizadas
    (<a href="http://www.koffice.org/krita/">Krita</a>);
  </li>
  <li>uma aplica&ccedil;&atilde;o de desenho vectorial
    (<a href="http://www.koffice.org/karbon/">Karbon14</a>).
  </li>
</ul>
<p>
  Adicionalmente, o KOffice inclui objectos incorporados robustos:
</p>
<ul>
  <li>uma aplica&ccedil;&atilde;o de relat&oacute;rios de qualidade empresarial
    (<a href="http://www.thekompany.com/projects/kugar/">Kugar</a>)
  </li>
  <li>um motor de gr&aacute;ficos de dados completo
    (<a href="http://www.koffice.org/kchart/">KChart</a>)
  </li>
  <li>tratamento de f&oacute;rmulas matem&aacute;ticas
    (<a href="http://www.koffice.org/kformula/">KFormula</a>)
  </li>
</ul>
<p>
  assim como um m&oacute;dulo de gest&atilde;o de sin&oacute;nimos (KThesaurus)
  e o suporte para diversos 
    <a href="http://www.koffice.org/filters/1.6/">formatos de ficheiros</a>.
</p>
<p align="justify">
  O KOffice e todos os seus componentes (incluindo o KDE) est&atilde;o dispon&iacute;veis
  <em><strong>de gra&ccedil;a</strong></em>, segundo as licen&ccedil;as <em>Open Source</em>,
  nas r&eacute;plicas de <a href="http://download.kde.org/unstable/koffice-1.6/">HTTP e FTP</a> do KDE.
</p>
